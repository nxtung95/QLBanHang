    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        if (jTextField2.getText() == "" || jTextField2.getText().length() > 3) {
            return;
        }
        double discount = Double.parseDouble(jTextField2.getText());
        double tmpTotalPrice = totalPrice - totalPrice * (discount / 100);
        tmpTotalPrice = Utils.roundUp(tmpTotalPrice);
        discountPrice = tmpTotalPrice;
        String totalAmountPrint = tmpTotalPrice != 0 ? Utils.formatCurrency(tmpTotalPrice) : "0";
        jLabel12.setText(totalAmountPrint);
    }//GEN-LAST:event_jTextField2KeyReleased

