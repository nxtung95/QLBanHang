    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String invoiceNo = jTextField1.getText();
        String customerName = jTextField3.getText();
        if ("".equals(customerName)) {
            JOptionPane.showMessageDialog(rootPane, "Tên khách hàng đang trống");
            return;
        }
        if (productInvoiceList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Cần thêm sản phẩm trước khi thanh toán");
            return;
        }
        int discount = 0;
        if (!"".equals(jTextField2.getText())) {
            discount = Integer.parseInt(jTextField2.getText());
        } else {
            discountPrice = totalPrice;
        }
        InvoiceController invoiceController = new InvoiceController();
        boolean result = invoiceController.add(invoiceNo, user, customerName, productInvoiceList, totalPrice, discount, discountPrice);
        if (result) {
            StaffHistoryController staffHistoryController = new StaffHistoryController();
            staffHistoryController.insert(user.getId(), "Thêm hóa đơn thành công, mã hóa đơn: " + invoiceNo);
            
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Thêm hóa đơn thành công. Bạn có muốn thực hiện tiếp không?");
            if (confirm != 0) {
                List<Invoice> invoiceList = invoiceController.search(null, null);
                paymentForm.setInvoiceList(invoiceList);
                this.setVisible(false);
            }
            clearData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm hóa đơn thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

