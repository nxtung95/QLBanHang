    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if ("".equals(jTextField4.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Bạn cần chọn số lượng sản phẩm");
            return;
        }
        String productName = (String) jComboBox1.getSelectedItem();
        String productId = products.stream().filter(pv -> productName.equalsIgnoreCase(pv.getName())).findFirst().get().getId();
        int quantity = Integer.parseInt(jTextField4.getText());
        double price = products.stream().filter(pv -> productName.equalsIgnoreCase(pv.getName())).findFirst().get().getPrice();
        
        StoreController storeController = new StoreController();
        int storeQuantity = storeController.getQuantity(user.getStore().getId(), productId);
        if (quantity > storeQuantity) {
            JOptionPane.showMessageDialog(rootPane, "Số lượng sản phẩm trong cửa hàng không đủ, thử lại");
            return;
        }
        
        double tmpPrice = quantity * price;
        totalPrice += tmpPrice;
        jLabel9.setText(Utils.formatCurrency(totalPrice));
        ProductInvoiceRequest p = new ProductInvoiceRequest(productId, productName, quantity, tmpPrice);
        productInvoiceList.add(p);
        setAddProduct(p, tmpPrice);
    }//GEN-LAST:event_jButton1ActionPerformed

