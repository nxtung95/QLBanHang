    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        ProductController productController = new ProductController();
        List<Product> products = productController.findAll();
        this.products = products;
        for (Product p : products) {
            jComboBox1.addItem(p.getName());
        }
        jComboBox1.setSelectedIndex(0);
        jTextField3.setText("");
        jTextField4.setText("1");
        String priceProduct = Utils.formatCurrency(products.get(0).getPrice());
        jLabel8.setText(priceProduct);
        
        // get quantity
        StoreController storeController = new StoreController();
        int quantity = storeController.getQuantity(user.getStore().getId(), products.get(0).getId());
        jLabel14.setText(String.valueOf(quantity));
        
        // get next invoiceId
        InvoiceController invoiceController = new InvoiceController();
        String invoiceId = invoiceController.getNextInvoiceId();
        jTextField1.setText(invoiceId);
        productInvoiceList = new ArrayList<>();
    }//GEN-LAST:event_formWindowOpened

