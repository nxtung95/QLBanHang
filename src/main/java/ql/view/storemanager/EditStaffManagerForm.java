    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String staffNo = jTextField1.getText();
        String name = jTextField3.getText();
        Date birthday = jDateChooser1.getDate();
        String address = jTextField5.getText();
        int rank = jComboBox1.getSelectedIndex();
        
        UserController userController = new UserController();
        boolean result = userController.update(staffNo, name, birthday, address, rank, user.getStore());
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật nhân viên thành công");
            List<User> staffList = userController.getAllStaff(user.getStore().getId(), "");
            this.staffManagerForm.setStaffList(staffList);
            this.staffManagerForm.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật nhân viên thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

