    public void setStaff(User user) {
        if (user instanceof Staff) {
            Staff staff = (Staff) user;
            jTextField1.setText(staff.getStaffNo());
            jTextField2.setText(staff.getUsername());
            jTextField3.setText(staff.getName());
            jDateChooser1.setDate(staff.getBirthday());
            jTextField5.setText(staff.getAddress());
            jComboBox1.setSelectedIndex(staff.getRank() == 0 ? 0 : 1);
            jTextField6.setText(user.getStore().getName());
        }
    }

